pub fn generate_random_name() -> String {
    let animal = NAMES
        .choose(&mut rand::thread_rng())
        .map(|s| *s)
        .unwrap_or("fox")
        .to_lowercase();
    let adjective = ADJECTIVES
        .choose(&mut rand::thread_rng())
        .map(|s| *s)
        .unwrap_or("puzzled")
        .to_lowercase();

    format!("{}-{}", adjective, animal)
}
